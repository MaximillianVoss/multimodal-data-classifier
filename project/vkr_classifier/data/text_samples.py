TEXT_TEMPLATES = (
    "{header} {subject}. {details}. {clause}. {final}.",
    "{header} {subject}, {details}. Дополнительно {clause}. {final}.",
    "{header} В документе отражено, что {subject}, {details}. {clause}. {final}.",
)


def build_text_dataset(seed: int, samples_per_class: int = 160) -> tuple[list[str], list[str]]:
    rng = Random(seed)
    dataset: list[tuple[str, str]] = []

    for label, blueprint in TEXT_BLUEPRINTS.items():
        generated: list[str] = []
        combinations = product(
            TEXT_TEMPLATES,
            blueprint["headers"],
            blueprint["subjects"],
            blueprint["details"],
            blueprint["clauses"],
            blueprint["finals"],
        )
        for template, header, subject, details, clause, final in combinations:
            generated.append(
                template.format(
                    header=header,
                    subject=subject,
                    details=details,
                    clause=clause,
                    final=final,
                )
            )
        rng.shuffle(generated)
        for text in generated[:samples_per_class]:
            dataset.append((text, label))

    rng.shuffle(dataset)
    texts = [text for text, _ in dataset]
    labels = [label for _, label in dataset]
    return texts, labels


def build_demo_texts() -> list[str]:
    texts, labels = build_text_dataset(seed=7, samples_per_class=4)
    seen: set[str] = set()
    demo: list[str] = []
    for text, label in zip(texts, labels, strict=False):
        if label in seen:
            continue
        demo.append(text)
        seen.add(label)
        if len(seen) == len(TEXT_BLUEPRINTS):
            break
    return demo


def build_demo_text_map() -> dict[str, str]:
    mapping: dict[str, str] = {}
    for label, blueprint in TEXT_BLUEPRINTS.items():
        mapping[label] = TEXT_TEMPLATES[0].format(
            header=blueprint["headers"][0],
            subject=blueprint["subjects"][0],
            details=blueprint["details"][0],
            clause=blueprint["clauses"][0],
            final=blueprint["finals"][0],
        )
    return mapping
