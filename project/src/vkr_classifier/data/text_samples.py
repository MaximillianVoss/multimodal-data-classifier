TEXT_TEMPLATES = (
    "{intro} {subject} {action} {object} после {event}, {result}.",
    "{subject} {action} {object} во время {event}, {result}.",
    "{intro} {subject} {action} {object}, {result}.",
)


def build_text_dataset(seed: int, samples_per_class: int = 120) -> tuple[list[str], list[str]]:
    rng = Random(seed)
    dataset: list[tuple[str, str]] = []

    for label, blueprint in TEXT_BLUEPRINTS.items():
        generated: list[str] = []
        combinations = product(
            TEXT_TEMPLATES,
            blueprint["intros"],
            blueprint["subjects"],
            blueprint["actions"],
            blueprint["objects"],
            blueprint["events"],
            blueprint["results"],
        )
        for template, intro, subject, action, obj, event, result in combinations:
            generated.append(
                template.format(
                    intro=intro,
                    subject=subject,
                    action=action,
                    object=obj,
                    event=event,
                    result=result,
                )
            )
        rng.shuffle(generated)
        for text in generated[:samples_per_class]:
            dataset.append((text, label))

    rng.shuffle(dataset)
    texts = [text for text, _ in dataset]
    labels = [label for _, label in dataset]
    return texts, labels


def build_demo_texts() -> list[str]:
    texts, labels = build_text_dataset(seed=7, samples_per_class=3)
    seen: set[str] = set()
    demo: list[str] = []
    for text, label in zip(texts, labels, strict=False):
        if label not in seen:
            demo.append(text)
            seen.add(label)
        if len(seen) == len(TEXT_BLUEPRINTS):
            break
    return demo

